weekday = int(input("enter the week(1-7):"))
if weekday == 1:
    print("Weekend is Monday")
elif weekday == 2 :
    print("Weekday is Tuesday")
elif weekday == 3 :
    print("Weekday is Wendsday")
elif weekday == 4 :
    print("Weekday is Thursday")
elif weekday == 5 :
    print("Weekday is Friday")
elif weekday == 6 :
    print("Weekday is Saturday")
elif weekday == 7 :
    print("Weekday is Sunday")
else:
    print("Weekday Number is not Available")
